SELECT LTRIM(TO_CHAR(START_DATE, 'MM'), '0') AS MONTH, CAR_ID, COUNT(*) AS RECORDS
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE CAR_ID IN (SELECT CAR_ID
                FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
                WHERE TO_CHAR(START_DATE, 'YYYY-MM') BETWEEN '2022-08' AND '2022-10'
                GROUP BY CAR_ID
                HAVING COUNT(CAR_ID) >= 5)
AND TO_CHAR(START_DATE, 'YYYY-MM') BETWEEN '2022-08' AND '2022-10'
GROUP BY CAR_ID, TO_CHAR(START_DATE, 'MM')
ORDER BY TO_CHAR(START_DATE, 'MM'), CAR_ID DESC